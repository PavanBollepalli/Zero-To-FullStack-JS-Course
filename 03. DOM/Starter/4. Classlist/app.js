// classlist
// add()
// remove()
// toggle()
const h1 = document.querySelector("h1")
console.log(h1.classList)
// h1.classList.add() to add an class for the specified element
h1.classList.add("class-1")
console.log(h1)
// h1.classList.remove() to remove a class from the element 
h1.classList.remove("class-1")
console.log(h1)